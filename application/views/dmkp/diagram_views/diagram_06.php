                                <div class="well well-small">
                                	A Strategic Filter is one of two components (the other being a Structural Filter) that leadership puts in place to help the response make sense of the information that flows in and can threaten to overwhelm. A Strategic Filter acts as a gateway that allows useful information into the response and rejects other information that would serve to distract. A Strategic Filter is informed by the Strategic Intent statement.<br />
For example, if leadership decided to focus only on the sectors of WASH and Health, then a Strategic Filter would accept information (resources, staff deployments, technical guidance) about WASH and Health but would reject information relating to Food, Non-Food Items (NFIs) or Child Protection.
