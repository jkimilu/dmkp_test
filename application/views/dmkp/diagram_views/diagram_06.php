                                <div class="well well-small">
                                	A Structural Filter is one of two components (the other being a Strategic Filter) that leadership puts in place to help the response team make sense of the information that flows in and can threaten to overwhelm. A Structural Filter is the mechanism that takes incoming information and channels it to the response staff who require that information. Functioning Structural Filters are crucial to ensure that the right staff receive the right information so that important information is not lost and that staff are not bombarded by unnecessary information.<br />
For example, the open Skype channel used by emergency communications staff provides a central hub for all relevant communication-related information to be shared and distributed to appropriate communications team members, who then make use of this information. This is an example of a Structural Filter.
