                        <h5>Overview of Changes to National Office Disaster Preparedness Process and Documents</h5>
                        <p class="desc">In July 2012, a revised National Office preparedness process and documents were finalized resulting in a number of critical changes.  The changes were based on extensive field research of effective preparedness practices both within and external to World Vision.  Key changes include:
                            • Several annual planning areas that used to be in the NDPP have been moved to the NO annual business planning process, such as scenario development, capacity building planning, etc.  All DM annual planning information will now be contained in the NO annual business plan in line with partnership practice.
                            • Only operational information is now included in the new Disaster Preparedness Plan (DPP) so that it can be converted into an initial response strategy.
                            • The new DPP is no longer an annual plan but should instead be developed only when a hazard is imminent.
                            • The NO DM Committee was replaced either with a NO risk analysis and management committee or the functions of risk analysis and management be included in the responsibilities of the SLT (which includes HEA when the risk is around disasters).
